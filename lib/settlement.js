exports.getCountry = () => {
  return new Promise(function(resolve, reject) {
    let inlineKeyboard = []
    const For = (number=0) => {
      if(option.country.length > number){
        inlineKeyboard.push([Markup.button.callback(option.country[number].title, `settlement_${number}`)])
        number++
        For(number)
      }else{
        let buttons = Markup.inlineKeyboard(inlineKeyboard)
        resolve(buttons)
      }
    }
    For()
  });
}

exports.getRegion = (countryNumber) => {
  return new Promise(function(resolve, reject) {
    let inlineKeyboard = []
    const For = (number=0) => {
      if(option.country[countryNumber].region.length > number){
        inlineKeyboard.push([Markup.button.callback(option.country[countryNumber].region[number].title, `settlement_${countryNumber}_${number}`)])
        number++
        For(number)
      }else{
        inlineKeyboard.push([Markup.button.callback('Назад', `settlement_back`)])
        let buttons = Markup.inlineKeyboard(inlineKeyboard)
        resolve(buttons)
      }
    }
    For()
  });
}

exports.getCity = (countryNumber, regionNumber) => {
  return new Promise(function(resolve, reject) {
    let inlineKeyboard = []
    const For = (number=0) => {
      if(option.country[countryNumber].region[regionNumber].city.length > number){
        inlineKeyboard.push([Markup.button.callback(option.country[countryNumber].region[regionNumber].city[number], `settlement_${countryNumber}_${regionNumber}_${number}`)])
        number++
        For(number)
      }else{
        inlineKeyboard.push([Markup.button.callback('Назад', `settlement_${countryNumber}`)])
        let buttons = Markup.inlineKeyboard(inlineKeyboard)
        resolve(buttons)
      }
    }
    For()
  });
}

exports.getText = (countryNumber, regionNumber, cityNumber) => {
  return new Promise(function(resolve, reject) {
    let countryTITLE = option.country[countryNumber].title
    let regionTITLE = option.country[countryNumber].region[regionNumber].title
    let cityTITLE = option.country[countryNumber].region[regionNumber].city[cityNumber]
    resolve(countryTITLE+', '+regionTITLE+', '+cityTITLE)
  });
}
